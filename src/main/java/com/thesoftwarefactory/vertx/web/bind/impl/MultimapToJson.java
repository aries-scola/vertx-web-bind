public class MultimapToJson {
	private class Path {
		
		private class Segment {
			private int index;
			private String name;

			public Segment(String segment) {
				Objects.requireNonNull(segment);
				
				// by default index is <0 
				this.index =-1;
				Matcher matcher = SEGMENT_PATTERN.matcher(segment);
				boolean match = matcher.matches();
				this.name = matcher.group(1);
				String strIndex = matcher.group(3);
				if (strIndex!=null) {
					this.index = Integer.parseInt(strIndex);
				}
			}
			
			public int index() {
				return index;
			}

			public boolean isArray() {
				return index>=0;
			}

			public String name() {
				return name;
			}

			/* (non-Javadoc)
			 * @see java.lang.Object#toString()
			 */
			@Override
			public String toString() {
				StringBuilder builder = new StringBuilder();
				builder.append("Segment [index=").append(index).append(", ");
				if (name != null)
					builder.append("name=").append(name);
				builder.append("]");
				return builder.toString();
			}
			
		}
		
		private Segment[] segments;
		private String separator;
		private String toString;

		public Path() {
			this.segments = new Segment[0];
			this.separator = ".";
		}
		
		public Path(String path, String separator) {
			Objects.requireNonNull(path);
			Objects.requireNonNull(separator);

			this.separator = separator;
			String[] tmpSegments = path.split(Pattern.quote(separator));
			this.segments = new Segment[tmpSegments.length];
			for (int i=0; i<tmpSegments.length; i++) {
				this.segments[i] = new Segment(tmpSegments[i]);
			}
		}
		
		public int commonIndex(Path path) {
			Objects.requireNonNull(path);

			int result = -1;
			int minSegmentLength = Math.min(this.segments().length, path.segments().length);
			for (int i=0; i<minSegmentLength; i++) {
				Segment segment = this.segments()[i];
				Segment otherSegment = path.segments()[i];
				if (!segment.name().equals(otherSegment.name())) {
					return result;
				}
				else if (segment.index()!=otherSegment.index()) {
					return i;
				}
				result = i;
			}
			return result;
		}
		
		public Segment[] segments() {
			return segments;
		}
		
		@Override
		public String toString() {
			if (toString==null) {
				StringBuilder builder = new StringBuilder();
				for (int i=0; i<segments.length; i++) {
					Segment segment = segments[i];
					if (i>0) {
						builder.append(separator);
					}
					builder.append(segment.name());
				}
				toString = builder.toString();
			}
			return toString;
		}
		
	}

	private static final Pattern SEGMENT_PATTERN = Pattern.compile("(.*?)(\\[(\\d)+])?");
	
	private JsonFactory jsonFactory;

	private Path.Segment getSegmentWithName(Path path, int index, String name) {
		Path.Segment segment = getSegment(path, index);
		return segment!=null && segment.name().equals(name) ? segment : null;
	}
	
	private Path.Segment getSegment(Path path, int index) {
		return path!=null && index<path.segments().length ? path.segments()[index] : null;
	}
	
	/**
	 * Close existing tags based on the last and current path.
	 * We've got to account for the following use cases:
	 *   - if the step is an array
	 *      - if this is the last segment
	 *         - if the segment of the currentPath      
	 *      - else if this is the first segment
	 *     
	 *      - else
	 *     
	 *      - call writeEndObject()
	 *      - if the current step is not another element of the array 
	 *         - call writeEndObject() and writeEndArray()
	 *   - else 
	 *      - if (!lastSegment)
	 *         - call writeEndObject();
	 *      - else 
	 *         - do nothing. 
	 *         // this is to account for the following case:
 	 *         // order[0].pid = 1
 	 *         // order[1].pid = 2
	 * 
	 * We process each path segment of the *last* path in *reverse* order until commonIndex
	 * The last segment that is processed is the one at position commonIndex 
	 * The steps are rather simple and we've got to account for the following use cases:
	 *       
	 * @param jsonGenerator
	 * @param lastPath
	 * @param currentPath
	 * @param commonSegmentUpTo	index of the last segment lastOPath and currentPath have in common
	 * @throws IOException
	 */
	private void closeExistingTags(JsonGenerator jsonGenerator, Path lastPath, Path currentPath, int commonSegmentUpTo) throws IOException {
		int length = lastPath.segments().length;
		// close existing tags if any
		for (int i=length-1; i>=commonSegmentUpTo && i>=0; i--) {
			Path.Segment segment = lastPath.segments()[i];
			boolean lastSegment = i==length-1;
			if (segment.isArray()) {
//				boolean firstSegment = i==commonSegmentUpTo;
				if (lastSegment) {
					Path.Segment currentSegment = getSegmentWithName(currentPath, i, segment.name());
					if (currentSegment==null) {
						jsonGenerator.writeEndArray();
					}
				}
				else {
					Path.Segment currentSegment = getSegmentWithName(currentPath, i, segment.name());
					if (currentSegment==null) {
					// there is no array in the current path
					// we've got to close the previous object and array
						jsonGenerator.writeEndObject();
						jsonGenerator.writeEndArray();
					}
					else { 
					// the current segment is the continuation of an existing array
						if (currentSegment.index()!=segment.index()) {
							jsonGenerator.writeEndObject();
						}
					}
				}
			}
			else {
				if (!lastSegment) {
					Path.Segment currentSegment = getSegmentWithName(currentPath, i, segment.name());
					if (currentSegment==null) {
						jsonGenerator.writeEndObject();
					}
				}
			}
		}
	}
	
	/**
	 * open existing tags based on the last and current path. 
	 * We process each path segment of the *current* path. 
	 * The first segment that is processed is the one at position commonIndex 
	 * The steps are rather simple and we've got to account for the following use cases:
	 *   - if the step is an array
	 *     - if this is the last segment
	 *       - if the segment of the currentPath      
	 *     - else if this is the first segment
	 *     
	 *     - else
	 *     
	 *     - call writeEndObject()
	 *     - if the current step is not another element of the array 
	 *       - call writeEndObject() and writeEndArray()
	 *   - else
	 *     - call writeEndObject();
	 *
	 */
	private void openAncestorTags(JsonGenerator jsonGenerator, Path lastPath, Path currentPath, int commonIndex) throws IOException {
		if (commonIndex<0) {
			commonIndex = 0;
		}
		int length = currentPath.segments().length;
		// open ancestor tags if any
		for (int i=commonIndex; i<=length-1 && i>=0; i++) {
			boolean isLastSegment = i==length-1;
			Path.Segment segment = currentPath.segments()[i];
			if (segment.isArray()) {
				Path.Segment lastSegment = getSegmentWithName(lastPath, i, segment.name());
				if (lastSegment==null) {
				// this is the first time we encounter this segment. 
					jsonGenerator.writeFieldName(segment.name());
					jsonGenerator.writeStartArray();
					if (!isLastSegment) {
						jsonGenerator.writeStartObject();
					}
				}
				else {
					if (segment.index()!=lastSegment.index()) {
						if (!isLastSegment) {
							jsonGenerator.writeStartObject();
						}
					}
				}
			}
			else {
				if (!isLastSegment) {
					Path.Segment lastSegment = getSegmentWithName(lastPath, i, segment.name());
					if (lastSegment==null) {
						jsonGenerator.writeFieldName(segment.name());
						jsonGenerator.writeStartObject();
					}
				}
			}
		}
	}

	/**
	 * generate the json based on the lastPath and the currentPath. 
	 * the lastPath helps us to keep track of were we are in json, so we know if we've got to close tags or open new one
	 * the algorithm is fairly basic and works as follow:
	 *   - determine the index called 'CI' that is common to both the lastPath and currentPath. 
	 *   - using the lastPath, starting from 'CI' loop through all remaining segments and properly close json tags.
	 *   - using the currentPath, starting from CI, loop through all remaining segments and properly open json tags. 
	 *     we use the flag Segment.isArray to determine if  
	 *   - 
	 * @param name
	 * @param values
	 * @param jsonGenerator
	 * @param lastPath
	 * @param currentPath
	 * @throws IOException 
	 */
	private void handle(List<String> values, JsonGenerator jsonGenerator, Path lastPath, Path currentPath) throws IOException {
		int commonIndex = lastPath.commonIndex(currentPath);

		// do we need to close existing tags ?
		closeExistingTags(jsonGenerator, lastPath, currentPath, commonIndex);
		// do we need to open any tags ?
		openAncestorTags(jsonGenerator, lastPath, currentPath, commonIndex);
		
		// continue an existing array?
		if (commonIndex>0) {
			// we'got a common prefix. if the last segment of the path is an array 
			// and the index has changed, we've got to add  a start object
			Path.Segment lastPathCommonSegment = lastPath.segments()[commonIndex-1];
			Path.Segment currentPathCommonSegment = currentPath.segments()[commonIndex-1];
			if (lastPathCommonSegment.index()!=currentPathCommonSegment.index()) {
//				jsonGenerator.writeEndObject();
//				jsonGenerator.writeStartObject();
			}
		}
		
		// set the value or values of the last segment
		Path.Segment lastSegment = currentPath.segments()[currentPath.segments().length-1];
		if (lastSegment.isArray()) {
			if (values.size()==1) {
				jsonGenerator.writeString(values.get(0));
			}
			else if (values.size()>1) {
				jsonGenerator.writeStartArray();
				for (String value: values) {
					jsonGenerator.writeString(value);
				}
				jsonGenerator.writeEndArray();
			}
		}
		else if (values.size()>1) {
			jsonGenerator.writeFieldName(lastSegment.name());
			jsonGenerator.writeStartArray();
			for (String value: values) {
				jsonGenerator.writeString(value);
			}
			jsonGenerator.writeEndArray();
		}
		else if (values.size()==1) {
			jsonGenerator.writeFieldName(lastSegment.name());
			jsonGenerator.writeString(values.get(0));
		}
		else {
			jsonGenerator.writeFieldName(lastSegment.name());
			jsonGenerator.writeNull();
		}
	}
	
	private JsonFactory jsonFactory() {
		if (jsonFactory==null) {
			jsonFactory = new JsonFactory();
		}
		return jsonFactory;
	}

	/**
	 * order.id=order-id
	 * &order.date=order-date
	 * &order.customer.id=customer-id
	 * &order.customer.name=customer-name
	 * 
	 * ArrayBinder notation can also be used as follow:
	 * order.id=order-id
	 * &order.date=order-date
	 * &order.customer.id=customer-id
	 * &order.customer.name=customer-name
	 * &order.customer.address.street=street
	 * &order.customer.address.zipcode=zipcode
	 * &order.line[0].product.id=product-id
	 * &order.line[0].quantity=qty
	 * &order.line[0].price=price
	 * &order.line[0].comment=comment
	 * &order.line[1].product.id=product-id
	 * &order.line[1].quantity=qty
	 * &order.line[1].price=price
	 * ...
	 * 
	 * @param map
	 * @return
	 */
	public String toJson(MultiMap map) {
		Objects.requireNonNull(map);
		
		try {
			StringWriter writer = new StringWriter();
			JsonGenerator jsonGenerator = jsonFactory().createGenerator(writer);
			toJson(map, jsonGenerator);
			jsonGenerator.flush();
			return writer.toString();
		} 
		catch (IOException e) {
			throw new RuntimeException(e);
		}
	}

	private void toJson(MultiMap map, JsonGenerator jsonGenerator) throws IOException {
		// important: names are ordered by ascending order
		Set<String> names = map.names();
		Path lastPath = new Path();
		jsonGenerator.writeStartObject();
		for (String name: names) {
			List<String> values = map.getAll(name);
			Path currentPath = new Path(name, ".");
			handle(values, jsonGenerator, lastPath, currentPath);
			lastPath = currentPath;
		}
		closeExistingTags(jsonGenerator, lastPath, null, -1);
		jsonGenerator.writeEndObject();
	}
	
}
