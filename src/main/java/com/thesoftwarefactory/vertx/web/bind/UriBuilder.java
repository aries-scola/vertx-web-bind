package com.thesoftwarefactory.vertx.web.bind;

import java.io.UnsupportedEncodingException;
import java.net.URI;
import java.net.URISyntaxException;
import java.net.URLDecoder;
import java.net.URLEncoder;
import java.nio.charset.Charset;
import java.nio.charset.StandardCharsets;
import java.util.ArrayList;
import java.util.Collection;
import java.util.HashMap;
import java.util.LinkedHashMap;
import java.util.Map;
import java.util.Map.Entry;
import java.util.Objects;

/**
 * Support class to build URIs.
 * According to the specification, a URI has the following syntax:
 * 		[scheme:]scheme-specific-part[#fragment] 
 * 
 * Furthermore, A hierarchical URI is subject to further parsing according to the syntax:
 * 		[scheme:][//authority][path][?query][#fragment] 
 * 
 * The authority component of a hierarchical URI is, if specified, either server-based or registry-based. 
 * A server-based authority parses according to the familiar syntax 
 * 		[user-info@]host[:port]
 * 
 * All told, then, a URI instance has the following nine components: 
 * 		Component					Type
 * 		----------------------------------
 * 		scheme						String
 * 		scheme-specific-part    	String
 * 		authority					String
 * 		user-info					String		
 * 		host						String
 * 		port						int
 * 		path						String
 * 		query						String
 * 		fragment					String
 * 
 * @author stephane
 *
 */
public class UriBuilder {

	
	public static interface Encoder {
		public String decode(String s);

		public String encode(String s);
		
		public <T extends Encoder> T withCharset(Charset charset);
	}

	
	public static class UrlEncoder implements Encoder {
		private Charset charset = null;
		
		public UrlEncoder(Charset charset) {
			Objects.requireNonNull(charset);
			
			this.charset = charset;
		}
		
		@Override
		public String decode(String s) {
			String result = null;
			if (s!=null && charset!=null) {
				try {
					result = URLDecoder.decode(s, charset.toString());
				}
				catch (UnsupportedEncodingException e) {
				}
			}
			return result;
		}

		@Override
		public String encode(String s) {
			String result = null;
			if (s!=null && charset!=null) {
				try {
					result = URLEncoder.encode(s, charset.toString());
				}
				catch (UnsupportedEncodingException e) {
				}
			}
			return result;
		}
		
		public Charset getCharset() {
			return charset;
		}

		@SuppressWarnings("unchecked")
		@Override
		public UrlEncoder withCharset(Charset charset) {
			return new UrlEncoder(charset);
		}
		
	}

	private final static Encoder DEFAULT_ENCODER = new UrlEncoder(Charset.forName("UTF-8"));
	private final static String PARAMETER_NAME_VALUE_SEPARATOR = "=";
	private final static String PARAMETER_SEPARATOR = "&";
	private final static String USER_INFO_SUFFIX = "@";
	private final static String PORT_PREFIX = ":";

	private Encoder encoder;
	private String fragment;
	private String host;
	private Map<String, Collection<String>> parameters;
	private String path;
	private int port = -1;
	private String scheme;
	private String schemeSpecificPart;
	private String userInfo;

	public UriBuilder() {
		this.encoder = DEFAULT_ENCODER;
	}

	public UriBuilder(String uri) throws URISyntaxException {
		this(new URI(uri));
	}

	public UriBuilder(URI uri) {
		this.encoder = new UrlEncoder(StandardCharsets.UTF_8);
		// start with mandatory fields
		this.setScheme(uri.getScheme());
		this.setSchemeSpecificPart(uri.getSchemeSpecificPart());
		this.setFragment(uri.getFragment());
		// then add optional fields for hierarchical uris
//		this.setAuthority(uri.getAuthority());
		this.setUserInfo(uri.getUserInfo());
		this.setHost(uri.getHost());
		this.setPort(uri.getPort());
		this.setPath(uri.getRawPath());
		this.setQuery(uri.getRawQuery());
	}

	/**
	 * Copy constructor
	 * 
	 * @param uriBuilder
	 */
	public UriBuilder(UriBuilder uriBuilder) {
		Objects.requireNonNull(uriBuilder);

		this.encoder = uriBuilder.encoder;
		this.fragment = uriBuilder.fragment;
		this.host = uriBuilder.host;
		if (uriBuilder.parameters!=null) {
			for (Entry<String, Collection<String>> entry: uriBuilder.parameters.entrySet()) {
				setQueryParameter(entry.getKey(), entry.getValue());
			}
		}
		this.path = uriBuilder.path;
		this.port = uriBuilder.port;
		this.scheme = uriBuilder.scheme;
		this.schemeSpecificPart = uriBuilder.schemeSpecificPart;
		this.userInfo = uriBuilder.userInfo;
	}

	public UriBuilder addQueryParameter(String name, Collection<String> values) {
		if (!Is.isEmpty(name) && !Is.isEmpty(values)) {
			for (String value: values) {
				addQueryParameter(name, value);
			}
		}
		return this;
	}

	public UriBuilder addQueryParameter(String name, String value) {
		if (!Is.isEmpty(name) && !Is.isEmpty(value)) {
			initParameters();
			Collection<String> values = parameters.get(name);
			if (values==null) {
				values = new ArrayList<>();
				parameters.put(name, values);
			}
			values.add(value);
		}
		return this;
	}

	public UriBuilder copy() {
		return new UriBuilder(this);
	}
	
	public String getAuthority() {
		StringBuilder builder = new StringBuilder();
		appendAuthority(builder);
		return builder.length()>0 ? builder.toString() : null;
	}

	/**
	 * the syntax of authority is [user-info@]host[:port]
	 * 
	 * @param result
	 */
	protected void appendAuthority(StringBuilder result) {
		if (userInfo!=null) {
			result.append(userInfo);
			result.append(USER_INFO_SUFFIX);
		}
		if (host!=null) {
			result.append(host);
		}
		if (port>-1) {
			result.append(PORT_PREFIX);
			result.append(port);
		}
	}

	public String getFragment() {
		return fragment;
	}
	
	public String getHost() {
		return host;
	}

	public String getPath() {
		return path;
	}

	public int getPort() {
		return port;
	}
	
	public String getScheme() {
		return scheme;
	}

	public String getSchemeSpecificPart() {
		return schemeSpecificPart;
	}

	public String getUserInfo() {
		return userInfo;
	}

	private void initParameters() {
		if (parameters==null) {
			parameters = new LinkedHashMap<>();
		}
	}

	public UriBuilder removeParameter(String... names) {
		if (parameters!=null) {
			if (!Is.isEmpty(names)) {
				for (String name: names) {
					if (!Is.isEmpty(name)) {
						parameters.remove(name);
					}
				}
			}
		}
		return this;
	}

	public UriBuilder setEncoder(Encoder encoder) {
		Objects.requireNonNull(encoder);
		
		this.encoder = encoder;
		return this;
	}

	/**
	 * @param fragment the fragment to set
	 */
	public UriBuilder setFragment(String fragment) {
		this.fragment = fragment;
		return this;
	}
	
	public UriBuilder setHost(String host) {
		this.host = host;
		return this;
	}

	public UriBuilder setPath(String path) {
		this.path = path;
		return this;
	}

	public UriBuilder setPort(int port) {
		this.port = port;
		return this;
	}
	
	public UriBuilder setQuery(String query) {
        if (!Is.isEmpty(query)) {
    		initParameters();
        	String[] tmpParameters = query.split(UriBuilder.PARAMETER_SEPARATOR);
        	if (!Is.isEmpty(tmpParameters)) {
		        for (String tmpParameter : tmpParameters) {
		            String[] tmpParameterNameValue = tmpParameter.split(UriBuilder.PARAMETER_NAME_VALUE_SEPARATOR, 2);
		            if (tmpParameterNameValue!=null && tmpParameterNameValue.length>0 && tmpParameterNameValue.length<3) {
			            String tmpParameterName = tmpParameterNameValue[0];
			            String tmpParameterValue = tmpParameterNameValue.length==2 ? tmpParameterNameValue[1] : null;
			            
			            String decodedParameterName = encoder.decode(tmpParameterName);
			            String decodedParameterValue = tmpParameterValue!=null ? encoder.decode(tmpParameterValue) : null;

			            addQueryParameter(decodedParameterName, decodedParameterValue);
		            }
		        }
        	}
        }
		return this;
	}

	public UriBuilder setQueryParameter(String name, Collection<String> values) {
		if (!Is.isEmpty(name) && !Is.isEmpty(values)) {
			if (parameters==null) {
				parameters = new HashMap<>();
			}
			Collection<String> valuesCopy = new ArrayList<>();
			valuesCopy.addAll(values);
			parameters.put(name, valuesCopy);
		}
		return this;
	}

	public UriBuilder setQueryParameter(String name, String value) {
		if (!Is.isEmpty(name) && !Is.isEmpty(value)) {
			if (parameters==null) {
				parameters = new HashMap<>();
			}
			Collection<String> values = new ArrayList<>();
			values.add(value);
			parameters.put(name, values);
		}
		return this;
	}

	/**
	 * @param scheme the scheme to set
	 */
	public UriBuilder setScheme(String scheme) {
		this.scheme = scheme;
		return this;
	}

	public UriBuilder setSchemeSpecificPart(String schemaSpecificPart) {
		this.schemeSpecificPart = schemaSpecificPart;
		return this;
	}

	public UriBuilder setUserInfo(String userInfo) {
		this.userInfo = userInfo;
		return this;
	}

	@Override
	public String toString() {
	    final StringBuilder queryBuilder = new StringBuilder();
		// query
	    if (this.parameters!=null) {
			boolean isFirstParameter = true;
	        for (String parameterName: parameters.keySet()) {
	        	if (!Is.isEmpty(parameterName)) {
	        		String encodedParameterName = encoder.encode(parameterName);
	        		Collection<String> parameterValues = parameters.get(parameterName);
		            for (String parameterValue : parameterValues) {			            	
		        		// output the parameter separator if applicable
		            	if (!isFirstParameter) {
		            		queryBuilder.append(UriBuilder.PARAMETER_SEPARATOR);
		            	}
		            	queryBuilder.append(encodedParameterName);
		            	queryBuilder.append(UriBuilder.PARAMETER_NAME_VALUE_SEPARATOR);

		            	String encodedParameterValue = parameterValue!=null ? encoder.encode(parameterValue) : null;
		            	if (!Is.isEmpty(encodedParameterValue)) {
		            		queryBuilder.append(encodedParameterValue);
		                }
		            	isFirstParameter = false;
		            }
	        	}
	        }
	    }
	    String query = queryBuilder.length()>0 ? queryBuilder.toString() : null;
	    try {
	    	return new URI(this.getScheme(), getUserInfo(), getHost(), getPort(), getPath(), query, getFragment())
	    			.toString()
	    			.replace("%25", "%")
	    			;
	    } catch (Exception ex) {
	        // can be safely ignored
	    }
	    return null;
	}

}
